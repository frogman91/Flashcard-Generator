var questArray = []; // Array to hold the questions after they are created


function shuffle(array) {
    var currentIndex = array.length, temporaryValue, randomIndex;
  
    // While there remain elements to shuffle...
    while (0 !== currentIndex) {
  
      // Pick a remaining element...
      randomIndex = Math.floor(Math.random() * currentIndex);
      currentIndex -= 1;
  
      //Swap it with the current element.
      temporaryValue = array[currentIndex];
      array[currentIndex] = array[randomIndex];
      array[randomIndex] = temporaryValue;
    }
  
    return array;
}

function makeQuest() {
    for (var i=0; i<numArray.length; i++) {
        questArray.push(new ClozeCard(pokemon[numArray[i]] + " was the " + ordinal_suffix_of(numArray[i]+1) + " Pokemon in the original 151.", pokemon[numArray[i]]));
    }
}

function askQuest() {
    inquirer.prompt([
        {
            type: "input",
            message: questArray[count].partial,
            name: "answer"
        }
    ]).then(function(resp) {
        // The answer was correct
        if (resp.answer.toLowerCase() === questArray[count].cloze.toLowerCase()) {
            console.log("Correct, SUGOI!!");
            score++; // Increase score
        }
        // The answer was incorrect
        else {
            console.log("Incorrect, DAME!");
            console.log("The correct answer was " + questArray[count].cloze)
        }
        count++;
        // If there are more presidents left, ask another question
        if (count < 10) {
            askQuest();
        }
        // Otherwise the game is over, display final score
        else {
            console.log("Quiz over. You got " + score + "/" + 10 + " correct.");
        }
    })
}

function ordinal_suffix_of(i) {
    var j = i % 10,
        k = i % 100;
    if (j == 1 && k != 11) {
        return i + "st";
    }
    if (j == 2 && k != 12) {
        return i + "nd";
    }
    if (j == 3 && k != 13) {
        return i + "rd";
    }
    return i + "th";
}

